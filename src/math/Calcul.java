package math;

/**
 * Class of calcul methods
 * 
 * @author Maxime Léau
 *
 */
public class Calcul {
	
	/**
	 * Addition two numbers
	 * @param a first number
	 * @param b second number
	 * @return result of addition
	 */
	public long addition(long a, long b)
	{
		return a + b;
	}
	
	/**
	 * tract two numbers
	 * @param a first number
	 * @param b second number
	 * @return
	 */
	public long soustraction(long a, long b)
	{
		return a - b;
	}

}
